<script>
document.addEventListener('DOMContentLoaded', function() {
  var form = document.getElementById('footer-contact-form');
  var popup = document.getElementById('contact-success-popup');
  if (form) {
    form.addEventListener('submit', function(e) {
      e.preventDefault();
      var formData = new FormData(form);
      var btn = form.querySelector('button[type=submit]');
      var btnText = btn.innerHTML;
      btn.disabled = true;
      btn.innerHTML = '<span style="background:#fff;border-radius:50%;width:24px;height:24px;display:inline-block;text-align:center;"><i class="fas fa-spinner fa-spin" style="color:#FF6E2A;"></i></span> Sending...';
      fetch('contact-handler.php', { // Always POST to the handler file!
        method: 'POST',
        body: formData,
        headers: { 'X-Requested-With': 'XMLHttpRequest' }
      })
      .then(r => r.json())
      .then(data => {
        btn.disabled = false;
        btn.innerHTML = btnText;
        if (data.success) {
          form.reset();
          popup.style.display = 'block';
          popup.style.opacity = 1;
          setTimeout(function() {
            popup.style.opacity = 0;
            setTimeout(function() {
              popup.style.display = 'none';
            }, 400);
          }, 2200);
        } else {
          let msg = 'Submission failed. Please try again.';
          if (data.error) msg += "\n\n[Debug] " + data.error;
          alert(msg);
        }
      })
      .catch((err) => {
        btn.disabled = false;
        btn.innerHTML = btnText;
        alert('Submission failed. Please try again. [Network error]');
      });
    });
  }
});
</script>